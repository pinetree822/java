    class TimerRunnable implements Runnable {
        
        // JLabel 선언
        private JLabel timerLabel;

        // 초기화
        public TimerRunnable(JLabel timerLabel) {
            this.timerLabel = timerLabel;
        }// TimerRunnable()

        @Override
        public void run() {
            int n=0;// 카운터 초기화
            while(true){
                timerLabel.setText(Integer.toString(n));// 레이블에 카운터값 표시
                n++;// 카운터 증가
                try {
                    Thread.sleep(1000);// 1초 대기
                } catch (InterruptedException e) {
                    return ; // 예외시 Thread 종료
                }
            }// while
            
        }// run()
        
    }// class TimerRunnable
    
    
    
    
    
