    public void find(){
       // 1. 검색 유형값 얻어오기 => comboType으로 부터
         int index=comboType.getSelectedIndex();
         //작성자 => 0   메시지=>1   글번호=>2
         if(index<0) return;
                
        //2. 검색어 얻어오기
        String keyword=this.tfFind.getText();
        if(keyword==null || keyword.trim().equals("")){
            showMsg("검색어를 입력하세요");
            tfFind.requestFocus();
            return;
        }
        
       // 3. dao의 findMemo(검색유형, 검색어) 호출       
       Vector<MemoDTO>  mv=dao.findMemo(index, keyword);
                
        //4. 결과값 받아서 table에 출력
        if(mv!=null){
            showTable(mv);
        }
    }//----------------------------------------
    
//    벡터의 내용을 꺼내와 jtable에 보여주는 메소드
    private void showTable(Vector<MemoDTO> mv) {
        //데이터=> 모델 DefaultTableModel
        Object[][]data=new Object[mv.size()][4];
        for(int i=0;i<data.length; i++){
            MemoDTO m=mv.get(i);
            data[i][0]=m.getIdx();
            data[i][1]=m.getName();
            data[i][2]=m.getMsg();
            data[i][3]=m.getWdate();
        }//for----------
        String[] colNames={"글번호","작성자","메모내용","작성일"};
        DefaultTableModel model=new DefaultTableModel(data, colNames);
        //View와 Model을 연결
        table.setModel(model);
        
    }//----------------------------------------
    
