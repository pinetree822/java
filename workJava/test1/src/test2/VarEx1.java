package test2;

public class VarEx1 {

	public static void main(String[] args) {

		int year = 0;	// 년도, 초기화(정의, 선언, 할당)
		int age = 14;// 나이
		System.out.println(year);
		System.out.println(age);
		
		
		year = age + 2000;	// age에 2000을 더한다.
		age = age + 1;			// age에 1을 더한다.
		System.out.println(year);
		System.out.println(age);
		
	}//public static void main(String[] args)

}//public class VarEx1

