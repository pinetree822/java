package test2;

// 형변환 방법
public class CastingEx1 {

	public static void main(String[] args) {
		double d = 85.4;
		int score = (int)d;// 정수형 변환
		
		System.out.println("score = " + score);
		System.out.println("d = " + d);
	}//main------------

}//class------------------

/*
score = 85
d = 85.4
*/
